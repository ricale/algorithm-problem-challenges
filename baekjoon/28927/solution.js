function solution([t1, e1, f1], [t2, e2, f2]) {
  const max = t1 * 3 + e1 * 20 + f1 * 120;
  const mel = t2 * 3 + e2 * 20 + f2 * 120;
  console.log(max > mel ? "Max" : max < mel ? "Mel" : "Draw");
}

