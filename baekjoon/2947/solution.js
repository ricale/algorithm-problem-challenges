function solution(...nums) {
  let result = "";
  let changed = false;
  while (true) {
    changed = false;
    for (let i = 0; i < 4; i++) {
      if (nums[i] > nums[i + 1]) {
        [nums[i], nums[i + 1]] = [nums[i + 1], nums[i]];
        changed = true;
        result += `${nums.join(" ")}\n`;
      }
    }
    if (!changed) {
      break;
    }
  }
  console.log(result);
}

